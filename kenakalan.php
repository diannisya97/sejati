                                        <p class="text-justify">Human Immunodeficiency Virus (HIV) adalah jenis virus yang menyerang sistem kekebalan tubuh sehingga daya tahan tubuh menurun. Acquired Immuno Deficiency Syndrome (AIDS) adalah sekumpulan gejala yang disebabkan oleh menurunnya sistem kekebalan tubuh manusia karena terinfeksi HIV. Pada tahap infeksi HIV belum tampak gejala-gejala pada penderita HIV positif, akan tetapi sudah dapat menularkan pada orang lain. Gejala AIDS muncul setelah 5-10 tahun, tergantung dari daya tahan tubuh penderita, gizi dan kepatuhan mengkonsumsi Antiretroviral (ARV).</p>

